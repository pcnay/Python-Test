"""
Practica 1:
Calcular "Cociente" y "Residuo (Resto) de dos numeros enteros."
"""
print ("Calcular Cociente y Residuo")
a = int(input("Ingresa el primer numero = "))
b = int(input("Ingresa el segundo numero = "))
cociente = a // b
residuo = a % b

print (f"Cociente es : {cociente} El residuo es: {residuo}")
