numero = int(input('Ingresa un numero del 0 al 9 : '))
if (numero == 0):
	print("CERO")
elif (numero == 1):
	print("UNO")
elif (numero == 2):
	print("DOS")
elif (numero == 3):
	print("TRES")
elif (numero == 4):
	print("CUATRO")
elif (numero == 5):
	print("CINCO")
elif (numero == 6):
	print("SEIS")
elif (numero == 7):
	print("SIETE")
elif (numero == 8):
	print("OCHO")
elif (numero == 9):
	print("NUEVE")
else:
	print ("Numero fuera de rango")
